package com.gyaltso.lcp.ladu.entitydsl.parser.antlr;

import java.io.InputStream;
import org.eclipse.xtext.parser.antlr.IAntlrTokenFileProvider;

public class EntityDslAntlrTokenFileProvider implements IAntlrTokenFileProvider {

	@Override
	public InputStream getAntlrTokenFile() {
		ClassLoader classLoader = getClass().getClassLoader();
		return classLoader.getResourceAsStream("com/gyaltso/lcp/ladu/entitydsl/parser/antlr/internal/InternalEntityDsl.tokens");
	}
}
